  function getMessage(locale, key, substitutions = []) {
    // Normalize locale (e.g., 'en-US' -> 'en')
    if (locale && locale.includes('-')) locale = locale.split('-')[0];
    if (!locale || !MESSAGES[locale]) locale = 'ar';
    let msg = MESSAGES[locale][key] || MESSAGES['ar'][key] || key;

    substitutions.forEach((sub, index) => {
      msg = msg.replaceAll(`$${index + 1}`, sub);
    });
    return msg;
  }

  function translateElement(el, locale) {
    const key = el.dataset.i18n;
    if (!key) return;
    const val = getMessage(locale, key);

    if (el.dataset.i18nHtml !== undefined) {
      const parser = new DOMParser();
      const doc = parser.parseFromString(val, 'text/html');
      el.textContent = '';
      while (doc.body.firstChild) {
        el.appendChild(doc.body.firstChild);
      }
      return;
    }
    if (el.dataset.i18nPlaceholder !== undefined) {
      el.placeholder = val;
      return;
    }
    if (el.dataset.i18nTitle !== undefined) {
      el.title = val;
      return;
    }
    // Support generic attributes
    if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, val);
      return;
    }
    el.textContent = val;
  }

  function translatePage(locale) {
    if (locale && locale.includes('-')) locale = locale.split('-')[0];
    if (!locale || !MESSAGES[locale]) locale = 'ar';
    document.documentElement.lang = locale;
    document.documentElement.dir = locale === 'ar' ? 'rtl' : 'ltr';
    document.querySelectorAll('[data-i18n]').forEach((el) => translateElement(el, locale));
  }

  return {
    getMessage,
    translatePage,
  };
