  function getMessage(locale, key, substitutions = []) {
    const lang = locale && MESSAGES[locale] ? locale : 'ar';
    const entry = MESSAGES[lang]?.[key] || MESSAGES.ar?.[key] || key;
    let msg = typeof entry === 'string' ? entry : entry.message || key;
    substitutions.forEach((sub, index) => {
      msg = msg.replace(new RegExp(`\\$${index + 1}`, 'g'), sub);
    });
    return msg;
  }

